
def animalInformation(type, legCount, sound, name):
    print("I am a " + type)
    print("I have " + legCount + " legs")
    print("My sound is: " + sound)
    print("My name is " + name)

animalInformation("chicken", 2, "cluck cluck", "Angel")
animalInformation("chicken", 2, "cluck cluck", "Coco")

animalInformation("dog", 4, "woof woof", "Rexx")
animalInformation("dog", 4, "woof woof", "Bingo")
